                <h4 class="card-title text-uppercase">Instrucciones como enlace</h4>
                <ol>
                    <li>Preliminares</li>
                    <ul>
                        <li>
                            Mandar mensaje de WhatsApp al moderador 1 hora antes con el acceso directo a la sala de Zoom en el caso de que sea enlace virtual. 
                            Los enlaces de <span class='text-primary'>ZOOM</span> y <span class='text-success'>Whatsapp</span> estarán disponibles dentro de sus <b>Horarios de ponencias</b>.
                        </li>
                        <li>
                            Revisar las ponencias dentro de <b>Horarios de ponencias</b>. Si existe alguna cuestion, contacte al equipo REDESLA.
                        </li>
                        <li>
                            <h4>Abrir video de introducción</h4>, tonos de aviso y cronómetro, los cuales puede encontrar en el siguiente enlace: 
                            <a href='https://drive.google.com/drive/u/0/folders/13QLc93jNthQW9Z4Iq80KTwMWh0xWtIW5' target='_blank'>https://drive.google.com/drive/u/0/folders/13QLc93jNthQW9Z4Iq80KTwMWh0xWtIW5</a>. 
                            <span class='text-warning'>Asegurese que sea la red correcta</span>. 
                        </li>
                        <li>
                            <h4>Carpeta de presentaciones Power Point</h4>: <a href="https://drive.google.com/drive/u/1/folders/1YD4JfyifgSJmme9Li0He9PEC74i75wG7">https://drive.google.com/drive/u/1/folders/1YD4JfyifgSJmme9Li0He9PEC74i75wG7</a>
                        </li>
                        <li>
                            Abrir la sala de <span class='text-primary'>ZOOM</span> 15 minutos antes y hacer las siguientes configuraciones:
                        </li>
                        <li>
                            <h4>Ponencias grabadas</h4>: <a href="https://drive.google.com/drive/u/1/folders/1V2DG3XOvOFmZFEq-lKgZnwbVuR6EXeWS" target="_blank">https://drive.google.com/drive/u/1/folders/1V2DG3XOvOFmZFEq-lKgZnwbVuR6EXeWS</a>
                        </li>
                        <ul>
                            <li>Que los participantes no puedan activar su micrófono.</li>
                            <li>Habilitar compartir pantalla para todos los participantes.</li>
                            <li>Que en el chat sólo puedan compartir con los moderadores</li>
                        </ul>
                    </ul>
                    <li>Bienvenida</li>
                    <ul>
                        <li>
                            Seguimiento al moderador
                        </li>
                        <li>
                            Dar bienvenida a participantes que llegan. Ejemplos:
                            <ul>
                                <li>Si el moderador está dando la bienvenida a cada participante decir: Buenos días, profesor. ¿va a presentar una ponencia? ¿Cuál es su ID? Gracias</li>
                                <li>Si el moderador no está dando la bienvenida: Buenos días, profesor. Bienvenido, ¿va a presentar una ponencia? ¿Cuál es su ID? Gracias</li>
                            </ul>
                            <span class='text-warning'>Nota: Anotar cuáles ponencias se van a presentar respetando el orden. Si alguno solicita un cambio de orden, se puede hacer con el consentimiento del moderador y de la sala.</span>
                        </li>
                        <li>
                            Dar inicio con video cuando lo indique el moderador (Compartir pantalla principal <span class='text-danger'>CON AUDIO</span>).
                        </li>
                        <li>
                            Al terminar el video decir: <span class='text-primary'>Agradecemos de nuevo su presencia, y le damos la palabra al moderador de la mesa, el Profesor _________.</span>
                        </li>
                        <li>
                            <span class='text-warning'>Moderador</span> - Bienvenida a nombre de la red
                        </li>
                        <li>
                            <span class='text-danger'>Hacerles notificar que el tiempo de presentación de su ponencia es de máximo 15 minutos.</span>
                        </li>
                    </ul>
                    <li>Presentación de ponencias</li>
                    <ul>
                        <li>
                            <span class='text-warning'>Moderador</span>: Da entrada al primer participante. A continuacion, el ponente dara inicio a su presentación.
                        </li>
                        <li>
                            Arrancar cronómetro en tu celular cuando inicie la presentación. El tiempo <span class='text-danger'>máximo</span> para cada presentación es de <span class='text-danger'>15 minutos</span>. 
                            <span class='text-danger'>Favor de hacerles notificar esto y hacer respetar el tiempo que se le brinda.</span>
                        </li>
                        <li>
                            Dar tonos de aviso de tiempos: 
                            <ul>
                                <li>Faltando 3 minutos: 1 timbre</li>
                                <li>Faltando 1 minuto: 2 timbres</li>
                                <li>Cuando el tiempo termine: timbres continuos</li>    
                            </ul>
                        </li>
                        <li>
                            Mientras el ponente este exponiendo, copiar el enlace de acceso a la ponencia para evaluar. Esto dando clic sobre el botón de <span class='text-primary'>Copiar accesos para zoom</span> 
                            ubicado en cada ponencia en su <b>Horario de ponencias</b>.
                        </li>
                        <li>
                            Al terminar la ponencia pegar el código de ponencia que se copió al dar clic sobre <span class='text-primary'>Copiar accesos para zoom</span> en el chat de <span class='text-primary'>ZOOM</span> y solicitar la evaluación de la misma. 
                            Irrumpir si es necesario.
                        </li>
                        <li><u>Repetir este proceso con cada ponencia.</u></li>
                    </ul>
                    <li>Ronda de preguntas y respuestas</li>
                    <ul>
                        <li>La conduce el moderador, los enlaces deben atender los micrófonos según lo solicite el moderador</li>
                        <li>Faltando 4 minutos, 2 minuto y 1 minuto para terminar el tiempo poner los tonos de aviso. Cuando termine el tiempo, el moderador cederá la palabra al enlace.</li>
                        <li>
                            Al finalizar el tiempo, cuando el moderador le de la palabra al enlace, favor de decir lo siguiente: <br>
                            <span class='text-primary'>
                            Agradecemos el apoyo del profesor __________ como moderador de la mesa. Les pedimos por favor que ahora pasen al auditorio virtual ~Nombre de la red~ para nuestra siguiente actividad.
                            </span>
                        </li>
                        <li>Apagar cámara (s).</li>
                        <li>Cerrar salón a la hora en punto.</li>
                    </ul>
                </ol>
